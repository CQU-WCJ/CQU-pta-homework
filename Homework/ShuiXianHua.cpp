#include <iostream>
#include <cmath> //pow函数需要这个头文件
using namespace std;

int main()
{
    long long n, a[1000], i = 0, sum = 0, n2;
    // n: 输入的数字, a[1000]: 存储每一位数字的数组, i: 数字位数计数器
    // sum: 重组后的数字, n2: 保存原始输入值用于后续比较

    cin >> n; // 输入要判断的数字
    n2 = n;   // 保存原始值，因为n在循环中会被修改

    // 将数字的每一位分解并存储到数组中
    while (n > 0)
    {
        a[i] = n % 10; // 取n的个位数（最后一位）
        n /= 10;       // 去掉已经处理的个位数
        i++;           // 位数计数器加1
    }

    // 将数组中的数字按逆序重新组合成新的数字
    for (long long j = 0; j < i; j++)
    {
        // 将每一位数字乘以对应的10的幂次，从最高位开始重组
        // i-j-1: 计算当前数字应该处于的位数（从高位到低位）
        sum += a[j] * pow(10, i - j - 1);
    }

    // 判断重组后的数字是否与原始数字相等
    if (sum == n2) // 如果相等，说明是回文数
    {
        cout << "是" << endl;
    }
    else // 否则不是回文数
        cout << "不是";
    return 0;
}
